#include <stdio.h>

int main(){
printf("CH.SC.U4CSE24130\n");

int integerVar = 10;
float floatVar = 20.5f;
double doubleVar = 30.123456;
char charVar = 'A';

printf("Integer: %d\n", integerVar);
printf("Float: %f\n", floatVar);
printf("Double: %lf\n", doubleVar);
printf("Character: %c\n", charVar);

return 0;
}

